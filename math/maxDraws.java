import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {

    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        int queries = in.nextInt();
        for(int i = 0; i < queries; i++){
            int x = in.nextInt();
            System.out.println(x+1);
        }
    }
}
